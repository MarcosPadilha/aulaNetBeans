    private void tblFornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFornecedorMouseClicked
        // TODO add your handling code here:
        
        if(tblFornecedor.getSelectedRow()>-1){
            int linha = tblFornecedor.getSelectedRow();
            DefaultTableModel conteudo = (DefaultTableModel) tblFornecedor.getModel();
            FornecedorBO c = FornecedorCL.findFornecedorCodigo(Integer.valueOf(conteudo.getValueAt(linha, 0).toString()));
            writeFornecedor(c);
          
        }
    }//GEN-LAST:event_tblFornecedorMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(txtCodigoFornecedor.getText());
        int linha = tblFornecedor.getSelectedRow();
        FornecedorCL.deleteFornecedor(codigo);
        ((DefaultTableModel) tblFornecedor.getModel()).removeRow(linha);
        limpaTelaFornecedor();
    }//GEN-LAST:event_btnExcluirActionPerformed

