    public void retrieveFornecedor(){
     DefaultTableModel modelo = new DefaultTableModel();
      modelo.addColumn("Código");
      modelo.addColumn("Nome");
      modelo.addColumn("telefone");
      modelo.addColumn("email");
      modelo.addColumn("Razao social");

         for(FornecedorBO c: FornecedorCL.showFornecedores()){
        modelo.addRow(new Object[] {
        c.getCodigo(), c.getNomeFantasia(), c.getTelefone(), c.geteMail(), c.getRazaoSocial()
        });
        }
            tblFornecedor.setModel(modelo);
    }
    public void writeFornecedor(FornecedorBO f){
            txtNomeFantasia.setText(f.getNomeFantasia());
            txtCodigoFornecedor.setText(String.valueOf(f.getCodigo()));
            txtRazaoSocial.setText(f.getRazaoSocial());
            txtEmailFornecedor.setText(f.geteMail());
            txtTelefoneFornecedor.setText(f.getTelefone());
            txtCNPJ.setText(String.valueOf(f.getCnpj().getNumero()));
            txtContato.setText(f.getContato());
            txtEstadoFornecedor.setText(f.getEndereco().getEstado());
            txtCidadeFornecedor.setText(f.getEndereco().getCidade());
            txtBairroFornecedor.setText(f.getEndereco().getBairro());
            txtLogradouroFornecedor.setText(f.getEndereco().getLogradouro());
            txtComplementoFornecedor.setText(f.getEndereco().getComplemento());
            txtNumeroFornecedor.setText(String.valueOf(f.getEndereco().getNumero()));
            txtCepFornecedor.setText(f.getEndereco().getCep());
            
    
    }
    
    
    
   

