    public FornecedorBO read(){
      int codigoFornecedor = Integer.parseInt(txtCodigoFornecedor.getText());
        String nomeFantasia =txtNomeFantasia.getText();
        String razao = txtRazaoSocial.getText();
        String email = txtEmailFornecedor.getText();
        String telefone = txtTelefoneFornecedor.getText();
        String cnpj = txtCNPJ.getText();
        CNPJBO cnpjFornecedor = new CNPJBO(cnpj);
        String contato = txtContato.getText();
        
        String estado = txtEstadoFornecedor.getText();
        String cidade = txtCidadeFornecedor.getText();
        String bairro = txtBairroFornecedor.getText();
        String logradouro = txtLogradouroFornecedor.getText();
        String complemento = txtComplementoFornecedor.getText();
        int numero = Integer.parseInt(txtNumeroFornecedor.getText());
        String cep = txtCepFornecedor.getText();
        
        EnderecoBO endereco = new EnderecoBO(estado, cidade, bairro, logradouro, complemento, numero, cep);
        
        FornecedorBO f = new FornecedorBO(codigoFornecedor, nomeFantasia, razao, email, telefone, cnpjFornecedor,endereco, contato );
    return f;
    }
    
